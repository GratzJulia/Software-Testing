package model;

public class Parametro {

	private String nome;
	private String tipoDeDado;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getTipoDeDado() {
		return tipoDeDado;
	}

	public void setTipoDeDado(String tipoDeDado) {
		this.tipoDeDado = tipoDeDado;
	}
}